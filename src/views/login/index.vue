<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">欢迎登录</h3>
      </div>

      <el-form-item prop="phone">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="phone"
          v-model="loginForm.phone"
          placeholder="账号"
          name="phone"
          type="text"
          tabindex="1"
          auto-complete="on"
          maxlength="11"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <template>
        <el-checkbox v-model="loginForm.remember">记住密码</el-checkbox>
      </template>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;margin-top: 20px" @click.native.prevent="handleLogin">登录</el-button>

<!--      <el-button type="text" @click="loginCode = true"><thirdPartyLogin /></el-button>-->

      <div class="tips">
        <span style="margin-right:20px;">账号: 15555555555</span>
        <span> 密码: 123456</span>
      </div>

    </el-form>

    <el-dialog
      title="请扫码登录"
      :visible.sync="loginCode"
      width="30%">
      <span>暂未开放</span>
    </el-dialog>

  </div>
</template>

<script>
import { validUsername } from '@/utils/validate'
import thirdPartyLogin from './components/thirdPartyLogin'

export default {
  name: 'Login',
  components: { thirdPartyLogin },
  data() {
    const validatePhone = (rule, value, callback) => {
      if (value === "") {
        callback(new Error("请输入手机号"));
      } else {
        if (!/^1[3456789]\d{9}$/.test(value)) {
          callback(new Error("请输入正确的手机号"));
        } else {
          callback();
        }
      }
    };
    return {
      loginForm: {
        phone: '',
        password: '',
        remember: null
      },
      loginRules: {
        phone: [
          { required: true, message: '请输入手机号', trigger: 'blur' },
          { validator: validatePhone, trigger: 'change' }
        ],
        password: [
          { required: true, message: '请输入密码', trigger: 'blur' },
          { min: 6, max: 14, message: '请输入6-14位密码', trigger: 'blur' }
        ],
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined,
      loginCode: false
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('admin/login', this.loginForm).then(() => {
            this.$router.push({ path: this.redirect || '/' })
            this.loading = false
          }).catch(() => {
            this.loading = false
          })
        } else {
          return false
        }
      })
    }
  },
  // mounted() {
  //   new WxLogin({
  //     id: "weixin",
  //     appid: "wx3bdb1192c22883f3",
  //     scope: "snsapi_login",
  //     // 扫码成功后 跳转的地址
  //     redirect_uri: "http://domain/weixinlogin"
  //   });
  // }
}
</script>

